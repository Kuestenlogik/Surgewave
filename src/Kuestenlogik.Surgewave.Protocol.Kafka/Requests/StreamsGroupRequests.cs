namespace Kuestenlogik.Surgewave.Protocol.Kafka.Requests;

// ────────────────────────────────────────────────────────────────
// StreamsGroupHeartbeat (API Key 88)
// ────────────────────────────────────────────────────────────────

/// <summary>
/// Kafka StreamsGroupHeartbeat request (API Key 88, v0-0).
/// KIP-1071: Streams group protocol — heartbeat for Kafka Streams applications.
/// </summary>
public sealed class StreamsGroupHeartbeatRequest : KafkaRequest
{
    /// <summary>The group identifier.</summary>
    public required string GroupId { get; init; }

    /// <summary>The member ID generated by the streams consumer.</summary>
    public required string MemberId { get; init; }

    /// <summary>The current member epoch; 0 to join; -1 to leave; -2 to rejoin as static member.</summary>
    public int MemberEpoch { get; init; }

    /// <summary>The current endpoint epoch of this client.</summary>
    public int EndpointInformationEpoch { get; init; }

    /// <summary>The instance ID for static membership, or null.</summary>
    public string? InstanceId { get; init; }

    /// <summary>The rack ID of the member, or null.</summary>
    public string? RackId { get; init; }

    /// <summary>The maximum time in ms that the coordinator will wait for task revocation. -1 if unchanged.</summary>
    public int RebalanceTimeoutMs { get; init; } = -1;

    /// <summary>The topology metadata. Only sent when memberEpoch=0. Null otherwise.</summary>
    public TopologyInfo? Topology { get; init; }

    /// <summary>Currently owned active tasks. Null if unchanged since last heartbeat.</summary>
    public List<TaskIds>? ActiveTasks { get; init; }

    /// <summary>Currently owned standby tasks. Null if unchanged since last heartbeat.</summary>
    public List<TaskIds>? StandbyTasks { get; init; }

    /// <summary>Currently owned warm-up tasks. Null if unchanged since last heartbeat.</summary>
    public List<TaskIds>? WarmupTasks { get; init; }

    /// <summary>Identity of the streams instance. Null if unchanged.</summary>
    public string? ProcessId { get; init; }

    /// <summary>User-defined endpoint for Interactive Queries. Null if unchanged.</summary>
    public EndpointInfo? UserEndpoint { get; init; }

    /// <summary>Used for rack-aware assignment algorithm. Null if unchanged.</summary>
    public List<KeyValuePair>? ClientTags { get; init; }

    /// <summary>Cumulative changelog offsets for tasks. Null if unchanged.</summary>
    public List<TaskOffsetInfo>? TaskOffsets { get; init; }

    /// <summary>Cumulative changelog end-offsets for tasks. Null if unchanged.</summary>
    public List<TaskOffsetInfo>? TaskEndOffsets { get; init; }

    /// <summary>Whether all Streams clients in the group should shut down.</summary>
    public bool ShutdownApplication { get; init; }

    // ─── Nested types ───────────────────────────────────────────

    public sealed class TopologyInfo
    {
        /// <summary>The epoch of the topology.</summary>
        public int Epoch { get; init; }

        /// <summary>The sub-topologies of the streams application.</summary>
        public required List<SubtopologyInfo> Subtopologies { get; init; }
    }

    public sealed class SubtopologyInfo
    {
        /// <summary>String to uniquely identify the subtopology.</summary>
        public required string SubtopologyId { get; init; }

        /// <summary>The topics the topology reads from.</summary>
        public required List<string> SourceTopics { get; init; }

        /// <summary>Regular expressions identifying topics the subtopology reads from.</summary>
        public required List<string> SourceTopicRegex { get; init; }

        /// <summary>State changelog topics associated with this subtopology.</summary>
        public required List<TopicInfo> StateChangelogTopics { get; init; }

        /// <summary>Repartition topics the subtopology writes to.</summary>
        public required List<string> RepartitionSinkTopics { get; init; }

        /// <summary>Repartition source topics. Created automatically.</summary>
        public required List<TopicInfo> RepartitionSourceTopics { get; init; }

        /// <summary>Subsets of source topics that must be copartitioned.</summary>
        public required List<CopartitionGroupInfo> CopartitionGroups { get; init; }
    }

    public sealed class CopartitionGroupInfo
    {
        /// <summary>Source topic indices into the subtopology-level array.</summary>
        public required List<short> SourceTopics { get; init; }

        /// <summary>Source topic regex indices into the subtopology-level array.</summary>
        public required List<short> SourceTopicRegex { get; init; }

        /// <summary>Repartition source topic indices into the subtopology-level array.</summary>
        public required List<short> RepartitionSourceTopics { get; init; }
    }

    public sealed class TopicInfo
    {
        /// <summary>The name of the topic.</summary>
        public required string Name { get; init; }

        /// <summary>The number of partitions (0 if not enforced).</summary>
        public int Partitions { get; init; }

        /// <summary>The replication factor (0 for default).</summary>
        public short ReplicationFactor { get; init; }

        /// <summary>Topic-level configurations.</summary>
        public required List<KeyValuePair> TopicConfigs { get; init; }
    }

    public sealed class KeyValuePair
    {
        /// <summary>Key of the config.</summary>
        public required string Key { get; init; }

        /// <summary>Value of the config.</summary>
        public required string Value { get; init; }
    }

    public sealed class EndpointInfo
    {
        /// <summary>Host of the endpoint.</summary>
        public required string Host { get; init; }

        /// <summary>Port of the endpoint.</summary>
        public ushort Port { get; init; }
    }

    public sealed class TaskIds
    {
        /// <summary>The subtopology identifier.</summary>
        public required string SubtopologyId { get; init; }

        /// <summary>The partitions of the input topics processed by this member.</summary>
        public required List<int> Partitions { get; init; }
    }

    public sealed class TaskOffsetInfo
    {
        /// <summary>The subtopology identifier.</summary>
        public required string SubtopologyId { get; init; }

        /// <summary>The partition.</summary>
        public int Partition { get; init; }

        /// <summary>The offset.</summary>
        public long Offset { get; init; }
    }

    public override void WriteTo(KafkaProtocolWriter writer)
    {
        // v0 is flexible
        writer.WriteInt16((short)ApiKey);
        writer.WriteInt16(ApiVersion);
        writer.WriteInt32(CorrelationId);
        writer.WriteCompactString(ClientId);
        writer.WriteVarInt(0); // Header tagged fields

        writer.WriteCompactString(GroupId);
        writer.WriteCompactString(MemberId);
        writer.WriteInt32(MemberEpoch);
        writer.WriteInt32(EndpointInformationEpoch);
        writer.WriteCompactString(InstanceId);
        writer.WriteCompactString(RackId);
        writer.WriteInt32(RebalanceTimeoutMs);

        // Topology (nullable struct)
        if (Topology == null)
        {
            writer.WriteVarInt(-1); // Null struct marker
        }
        else
        {
            writer.WriteVarInt(0); // Presence marker
            writer.WriteInt32(Topology.Epoch);
            writer.WriteVarInt(Topology.Subtopologies.Count + 1);
            foreach (var sub in Topology.Subtopologies)
            {
                writer.WriteCompactString(sub.SubtopologyId);

                writer.WriteVarInt(sub.SourceTopics.Count + 1);
                foreach (var topic in sub.SourceTopics) writer.WriteCompactString(topic);

                writer.WriteVarInt(sub.SourceTopicRegex.Count + 1);
                foreach (var regex in sub.SourceTopicRegex) writer.WriteCompactString(regex);

                writer.WriteVarInt(sub.StateChangelogTopics.Count + 1);
                foreach (var ti in sub.StateChangelogTopics)
                {
                    WriteTopicInfo(writer, ti);
                }

                writer.WriteVarInt(sub.RepartitionSinkTopics.Count + 1);
                foreach (var topic in sub.RepartitionSinkTopics) writer.WriteCompactString(topic);

                writer.WriteVarInt(sub.RepartitionSourceTopics.Count + 1);
                foreach (var ti in sub.RepartitionSourceTopics)
                {
                    WriteTopicInfo(writer, ti);
                }

                writer.WriteVarInt(sub.CopartitionGroups.Count + 1);
                foreach (var cg in sub.CopartitionGroups)
                {
                    writer.WriteVarInt(cg.SourceTopics.Count + 1);
                    foreach (var idx in cg.SourceTopics) writer.WriteInt16(idx);

                    writer.WriteVarInt(cg.SourceTopicRegex.Count + 1);
                    foreach (var idx in cg.SourceTopicRegex) writer.WriteInt16(idx);

                    writer.WriteVarInt(cg.RepartitionSourceTopics.Count + 1);
                    foreach (var idx in cg.RepartitionSourceTopics) writer.WriteInt16(idx);

                    writer.WriteVarInt(0); // CopartitionGroup tagged fields
                }

                writer.WriteVarInt(0); // Subtopology tagged fields
            }
            writer.WriteVarInt(0); // Topology tagged fields
        }

        WriteNullableTaskIdsList(writer, ActiveTasks);
        WriteNullableTaskIdsList(writer, StandbyTasks);
        WriteNullableTaskIdsList(writer, WarmupTasks);

        writer.WriteCompactString(ProcessId);

        // UserEndpoint (nullable struct)
        if (UserEndpoint == null)
        {
            writer.WriteVarInt(-1); // Null struct marker
        }
        else
        {
            writer.WriteVarInt(0); // Presence marker
            writer.WriteCompactString(UserEndpoint.Host);
            writer.WriteInt16((short)UserEndpoint.Port);
            writer.WriteVarInt(0); // Endpoint tagged fields
        }

        WriteNullableKeyValueList(writer, ClientTags);
        WriteNullableTaskOffsetList(writer, TaskOffsets);
        WriteNullableTaskOffsetList(writer, TaskEndOffsets);

        writer.WriteBoolean(ShutdownApplication);

        writer.WriteVarInt(0); // Body tagged fields
    }

    private static void WriteTopicInfo(KafkaProtocolWriter writer, TopicInfo ti)
    {
        writer.WriteCompactString(ti.Name);
        writer.WriteInt32(ti.Partitions);
        writer.WriteInt16(ti.ReplicationFactor);
        writer.WriteVarInt(ti.TopicConfigs.Count + 1);
        foreach (var kv in ti.TopicConfigs)
        {
            writer.WriteCompactString(kv.Key);
            writer.WriteCompactString(kv.Value);
            writer.WriteVarInt(0); // KeyValue tagged fields
        }
        writer.WriteVarInt(0); // TopicInfo tagged fields
    }

    private static void WriteNullableTaskIdsList(KafkaProtocolWriter writer, List<TaskIds>? list)
    {
        if (list == null)
        {
            writer.WriteVarInt(-1);
        }
        else
        {
            writer.WriteVarInt(list.Count + 1);
            foreach (var task in list)
            {
                writer.WriteCompactString(task.SubtopologyId);
                writer.WriteVarInt(task.Partitions.Count + 1);
                foreach (var p in task.Partitions) writer.WriteInt32(p);
                writer.WriteVarInt(0); // TaskIds tagged fields
            }
        }
    }

    private static void WriteNullableKeyValueList(KafkaProtocolWriter writer, List<KeyValuePair>? list)
    {
        if (list == null)
        {
            writer.WriteVarInt(-1);
        }
        else
        {
            writer.WriteVarInt(list.Count + 1);
            foreach (var kv in list)
            {
                writer.WriteCompactString(kv.Key);
                writer.WriteCompactString(kv.Value);
                writer.WriteVarInt(0); // KeyValue tagged fields
            }
        }
    }

    private static void WriteNullableTaskOffsetList(KafkaProtocolWriter writer, List<TaskOffsetInfo>? list)
    {
        if (list == null)
        {
            writer.WriteVarInt(-1);
        }
        else
        {
            writer.WriteVarInt(list.Count + 1);
            foreach (var to in list)
            {
                writer.WriteCompactString(to.SubtopologyId);
                writer.WriteInt32(to.Partition);
                writer.WriteInt64(to.Offset);
                writer.WriteVarInt(0); // TaskOffset tagged fields
            }
        }
    }

    public static StreamsGroupHeartbeatRequest ReadFrom(KafkaProtocolReader reader, short apiVersion, int correlationId, string clientId)
    {
        var groupId = reader.ReadCompactString() ?? "";
        var memberId = reader.ReadCompactString() ?? "";
        var memberEpoch = reader.ReadInt32();
        var endpointInformationEpoch = reader.ReadInt32();
        var instanceId = reader.ReadCompactString();
        var rackId = reader.ReadCompactString();
        var rebalanceTimeoutMs = reader.ReadInt32();

        // Topology (nullable struct) — peek at first field presence
        // For a nullable struct, the Kafka protocol uses a special marker
        TopologyInfo? topology = null;
        // We read the topology epoch — if the struct is present
        // In the flexible protocol, a nullable struct is encoded similarly to a nullable array
        // We need to check for null by reading the "presence" byte
        // Actually, for a nullable struct, we just try reading fields — if it was null,
        // the writer would have written -1 as a varint. But struct encoding is different from arrays.
        // Per Kafka protocol spec: nullable struct uses varint -1 for absent, otherwise fields follow directly.
        // Let's check: the struct presence is signaled like a nullable field.
        // Looking at the pattern from ConsumerGroupHeartbeat, for nullable structs the approach
        // used is ReadVarInt to detect presence.
        var topoMarker = reader.ReadVarInt();
        if (topoMarker >= 0)
        {
            // topoMarker is actually the Epoch int32 first byte — we need a different approach.
            // Nullable structs in Kafka's flexible protocol: -1 means null, otherwise the struct fields follow.
            // But we already consumed the marker. The marker IS the epoch if present.
            // Actually, looking more carefully: the nullable struct approach uses 0xFF byte for null.
            // Let's handle this pragmatically: we already read a varint. If it was -1, null. Otherwise
            // it's actually the topology epoch read as varint.
            // Re-examining: In the Kafka protocol for nullable structs (not arrays), a boolean-like presence
            // indicator is used: -1 = absent. Since we wrote with WriteVarInt(-1) for null, we need to
            // treat the first varint as a presence marker and then read the Epoch separately.
            // But wait: on the write side we wrote Topology.Epoch as WriteInt32 AFTER the null check.
            // So the varint(-1) indicates null, and if not null, the next bytes are the Epoch as Int32.
            // Since we used WriteVarInt(-1) for null, we need to UNDO this read and re-read as Int32.
            // This is tricky. Let's look at what we actually wrote:
            // null: WriteVarInt(-1)  -> reads as ReadVarInt() == -1
            // non-null: WriteInt32(Epoch) -> reads as ReadVarInt() which consumes some bytes of the Int32
            // This is WRONG in the write path. We need to fix the write path or accept the mismatch.
            // Actually, looking at the ConsumerGroupHeartbeat pattern more carefully:
            // For Assignment (nullable struct), the pattern is:
            //   write: WriteVarInt(-1) for null, or WriteVarInt(count+1) for the array inside
            //   This works because Assignment has exactly one array field (TopicPartitions).
            // For Topology, which is a proper struct with non-array first field (Epoch: int32),
            // the Kafka protocol uses a DIFFERENT pattern: present/absent byte.
            // Given our write path writes WriteVarInt(-1) for null and then raw fields for non-null,
            // we should keep ReadVarInt for the null check.
            // HOWEVER, we wrote the epoch as Int32 after checking null, not as the varint marker.
            // So the -1 varint (0x01 byte for unsigned zigzag) would collide with the Int32 epoch.
            // Let's fix this: the correct approach for nullable struct is to use a 1-byte presence indicator.
            // For now, in our implementation, we write a varint presence marker:
            //   -1 = null, 0 = present (then fields follow)
            // But our write code doesn't write 0 for present — it goes directly to fields.
            // We need to fix the writer to emit a presence marker. Let's handle this by
            // expecting that our WriteTo was correct (it wrote -1 for null, then fields directly).
            // Since we need to match our own wire format, the topology presence check is:
            // We wrote: [varint(-1)] or [int32(epoch), rest...]. The ReadVarInt() will consume
            // a variable number of bytes from the int32. This is a bug in WriteTo.
            // For correctness, let's fix the approach: use a boolean-style presence varint.

            // Since we control both reader and writer, and the writer wrote Int32(Epoch) directly
            // after the null path, this means the first read after the null branch is Int32(Epoch).
            // But we already consumed bytes with ReadVarInt(). This won't work correctly for
            // arbitrary epoch values.
            //
            // PRAGMATIC FIX: Let's re-interpret. We'll treat this as if the writer writes a
            // varint presence flag (0 or -1), then the struct fields. We need to adjust WriteTo too.
            // For now, since this is the ReadFrom and must match WriteTo, let's assume the topoMarker
            // we read IS actually the varint presence marker. We'll adjust WriteTo to write
            // varint(0) before the struct fields when non-null.
            //
            // Since we ALSO need to fix WriteTo later, let's assume the current wire format is:
            //   null: varint(-1)
            //   non-null: varint(0) + fields (int32 epoch, ...)
            // The topoMarker == 0 means present.

            var epoch = reader.ReadInt32();
            var subCount = reader.ReadVarInt() - 1;
            var subtopologies = new List<SubtopologyInfo>(subCount);
            for (int i = 0; i < subCount; i++)
            {
                var subtopologyId = reader.ReadCompactString() ?? "";

                var srcCount = reader.ReadVarInt() - 1;
                var sourceTopics = new List<string>(srcCount);
                for (int j = 0; j < srcCount; j++) sourceTopics.Add(reader.ReadCompactString() ?? "");

                var regexCount = reader.ReadVarInt() - 1;
                var sourceTopicRegex = new List<string>(regexCount);
                for (int j = 0; j < regexCount; j++) sourceTopicRegex.Add(reader.ReadCompactString() ?? "");

                var sclCount = reader.ReadVarInt() - 1;
                var stateChangelogTopics = new List<TopicInfo>(sclCount);
                for (int j = 0; j < sclCount; j++) stateChangelogTopics.Add(ReadTopicInfo(reader));

                var rskCount = reader.ReadVarInt() - 1;
                var repartitionSinkTopics = new List<string>(rskCount);
                for (int j = 0; j < rskCount; j++) repartitionSinkTopics.Add(reader.ReadCompactString() ?? "");

                var rsrcCount = reader.ReadVarInt() - 1;
                var repartitionSourceTopics = new List<TopicInfo>(rsrcCount);
                for (int j = 0; j < rsrcCount; j++) repartitionSourceTopics.Add(ReadTopicInfo(reader));

                var cgCount = reader.ReadVarInt() - 1;
                var copartitionGroups = new List<CopartitionGroupInfo>(cgCount);
                for (int j = 0; j < cgCount; j++)
                {
                    var cgSrc = ReadShortList(reader);
                    var cgRegex = ReadShortList(reader);
                    var cgRsrc = ReadShortList(reader);
                    reader.SkipTaggedFields();

                    copartitionGroups.Add(new CopartitionGroupInfo
                    {
                        SourceTopics = cgSrc,
                        SourceTopicRegex = cgRegex,
                        RepartitionSourceTopics = cgRsrc
                    });
                }

                reader.SkipTaggedFields();

                subtopologies.Add(new SubtopologyInfo
                {
                    SubtopologyId = subtopologyId,
                    SourceTopics = sourceTopics,
                    SourceTopicRegex = sourceTopicRegex,
                    StateChangelogTopics = stateChangelogTopics,
                    RepartitionSinkTopics = repartitionSinkTopics,
                    RepartitionSourceTopics = repartitionSourceTopics,
                    CopartitionGroups = copartitionGroups
                });
            }
            reader.SkipTaggedFields(); // Topology tagged fields

            topology = new TopologyInfo
            {
                Epoch = epoch,
                Subtopologies = subtopologies
            };
        }

        var activeTasks = ReadNullableTaskIdsList(reader);
        var standbyTasks = ReadNullableTaskIdsList(reader);
        var warmupTasks = ReadNullableTaskIdsList(reader);

        var processId = reader.ReadCompactString();

        // UserEndpoint (nullable struct)
        EndpointInfo? userEndpoint = null;
        var epMarker = reader.ReadVarInt();
        if (epMarker >= 0)
        {
            var host = reader.ReadCompactString() ?? "";
            var port = (ushort)reader.ReadInt16();
            reader.SkipTaggedFields();
            userEndpoint = new EndpointInfo { Host = host, Port = port };
        }

        var clientTags = ReadNullableKeyValueList(reader);
        var taskOffsets = ReadNullableTaskOffsetList(reader);
        var taskEndOffsets = ReadNullableTaskOffsetList(reader);
        var shutdownApplication = reader.ReadBoolean();

        reader.SkipTaggedFields();

        return new StreamsGroupHeartbeatRequest
        {
            ApiKey = ApiKey.StreamsGroupHeartbeat,
            ApiVersion = apiVersion,
            CorrelationId = correlationId,
            ClientId = clientId,
            GroupId = groupId,
            MemberId = memberId,
            MemberEpoch = memberEpoch,
            EndpointInformationEpoch = endpointInformationEpoch,
            InstanceId = instanceId,
            RackId = rackId,
            RebalanceTimeoutMs = rebalanceTimeoutMs,
            Topology = topology,
            ActiveTasks = activeTasks,
            StandbyTasks = standbyTasks,
            WarmupTasks = warmupTasks,
            ProcessId = processId,
            UserEndpoint = userEndpoint,
            ClientTags = clientTags,
            TaskOffsets = taskOffsets,
            TaskEndOffsets = taskEndOffsets,
            ShutdownApplication = shutdownApplication
        };
    }

    private static TopicInfo ReadTopicInfo(KafkaProtocolReader reader)
    {
        var name = reader.ReadCompactString() ?? "";
        var partitions = reader.ReadInt32();
        var replicationFactor = reader.ReadInt16();
        var cfgCount = reader.ReadVarInt() - 1;
        var topicConfigs = new List<KeyValuePair>(cfgCount);
        for (int i = 0; i < cfgCount; i++)
        {
            var key = reader.ReadCompactString() ?? "";
            var value = reader.ReadCompactString() ?? "";
            reader.SkipTaggedFields();
            topicConfigs.Add(new KeyValuePair { Key = key, Value = value });
        }
        reader.SkipTaggedFields();

        return new TopicInfo
        {
            Name = name,
            Partitions = partitions,
            ReplicationFactor = replicationFactor,
            TopicConfigs = topicConfigs
        };
    }

    private static List<short> ReadShortList(KafkaProtocolReader reader)
    {
        var count = reader.ReadVarInt() - 1;
        var list = new List<short>(count);
        for (int i = 0; i < count; i++) list.Add(reader.ReadInt16());
        return list;
    }

    private static List<TaskIds>? ReadNullableTaskIdsList(KafkaProtocolReader reader)
    {
        var count = reader.ReadVarInt() - 1;
        if (count < 0) return null;
        var list = new List<TaskIds>(count);
        for (int i = 0; i < count; i++)
        {
            var subtopologyId = reader.ReadCompactString() ?? "";
            var partCount = reader.ReadVarInt() - 1;
            var partitions = new List<int>(partCount);
            for (int j = 0; j < partCount; j++) partitions.Add(reader.ReadInt32());
            reader.SkipTaggedFields();
            list.Add(new TaskIds { SubtopologyId = subtopologyId, Partitions = partitions });
        }
        return list;
    }

    private static List<KeyValuePair>? ReadNullableKeyValueList(KafkaProtocolReader reader)
    {
        var count = reader.ReadVarInt() - 1;
        if (count < 0) return null;
        var list = new List<KeyValuePair>(count);
        for (int i = 0; i < count; i++)
        {
            var key = reader.ReadCompactString() ?? "";
            var value = reader.ReadCompactString() ?? "";
            reader.SkipTaggedFields();
            list.Add(new KeyValuePair { Key = key, Value = value });
        }
        return list;
    }

    private static List<TaskOffsetInfo>? ReadNullableTaskOffsetList(KafkaProtocolReader reader)
    {
        var count = reader.ReadVarInt() - 1;
        if (count < 0) return null;
        var list = new List<TaskOffsetInfo>(count);
        for (int i = 0; i < count; i++)
        {
            var subtopologyId = reader.ReadCompactString() ?? "";
            var partition = reader.ReadInt32();
            var offset = reader.ReadInt64();
            reader.SkipTaggedFields();
            list.Add(new TaskOffsetInfo { SubtopologyId = subtopologyId, Partition = partition, Offset = offset });
        }
        return list;
    }
}

/// <summary>
/// Kafka StreamsGroupHeartbeat response (API Key 88, v0-0).
/// </summary>
public sealed class StreamsGroupHeartbeatResponse : KafkaResponse
{
    /// <summary>Duration in milliseconds for which the request was throttled.</summary>
    public int ThrottleTimeMs { get; init; }

    /// <summary>The top-level error code, or 0 if there was no error.</summary>
    public ErrorCode ErrorCode { get; init; }

    /// <summary>The top-level error message, or null if there was no error.</summary>
    public string? ErrorMessage { get; init; }

    /// <summary>The member ID.</summary>
    public required string MemberId { get; init; }

    /// <summary>The member epoch.</summary>
    public int MemberEpoch { get; init; }

    /// <summary>The heartbeat interval in milliseconds.</summary>
    public int HeartbeatIntervalMs { get; init; }

    /// <summary>The maximal lag a warm-up task can have to be considered caught-up.</summary>
    public int AcceptableRecoveryLag { get; init; }

    /// <summary>The interval in which task changelog offsets are updated on the broker.</summary>
    public int TaskOffsetIntervalMs { get; init; }

    /// <summary>Zero or more status for the group.</summary>
    public List<StatusInfo>? Status { get; init; }

    /// <summary>Assigned active tasks. Null if unchanged.</summary>
    public List<TaskIds>? ActiveTasks { get; init; }

    /// <summary>Assigned standby tasks. Null if unchanged.</summary>
    public List<TaskIds>? StandbyTasks { get; init; }

    /// <summary>Assigned warm-up tasks. Null if unchanged.</summary>
    public List<TaskIds>? WarmupTasks { get; init; }

    /// <summary>The endpoint epoch set in the response.</summary>
    public int EndpointInformationEpoch { get; init; }

    /// <summary>Global assignment information used for IQ. Null if unchanged.</summary>
    public List<EndpointToPartitionsInfo>? PartitionsByUserEndpoint { get; init; }

    // ─── Nested types ───────────────────────────────────────────

    public sealed class StatusInfo
    {
        /// <summary>A code to indicate a particular status is active for the group membership.</summary>
        public sbyte StatusCode { get; init; }

        /// <summary>A string representation of the status.</summary>
        public required string StatusDetail { get; init; }
    }

    public sealed class TaskIds
    {
        /// <summary>The subtopology identifier.</summary>
        public required string SubtopologyId { get; init; }

        /// <summary>The partitions of the input topics processed by this member.</summary>
        public required List<int> Partitions { get; init; }
