    public sealed class EndpointToPartitionsInfo
    {
        /// <summary>User-defined endpoint to connect to the node.</summary>
        public required EndpointInfo UserEndpoint { get; init; }

        /// <summary>All topic partitions materialized by active tasks on the node.</summary>
        public required List<TopicPartition> ActivePartitions { get; init; }

        /// <summary>All topic partitions materialized by standby tasks on the node.</summary>
        public required List<TopicPartition> StandbyPartitions { get; init; }
    }

    public sealed class EndpointInfo
    {
        /// <summary>Host of the endpoint.</summary>
        public required string Host { get; init; }

        /// <summary>Port of the endpoint.</summary>
        public ushort Port { get; init; }
